// This function enables calculation of dog's age vs human. 
function calculateDogAge(humanAge) {
    return humanAge * 7;
}

// The following code tests the function. It can be viewed in the console (Developer tool) of a browser. 
console.log("A 5 year old person is " + calculateDogAge(5) + " years old in dog years.");
console.log("A 10 year old person is " + calculateDogAge(10) + " years old in dog years.");
console.log("A 25 year old person is " + calculateDogAge(25) + " years old in dog years.");